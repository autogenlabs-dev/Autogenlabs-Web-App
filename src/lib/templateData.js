export const defaultCurrency = currencies.find(c => c.code === 'USD');

// Helper functions
export const getCurrencyByCode = (code) => {
  return currencies.find(c => c.code === code) || defaultCurrency;
};

export const formatPrice = (amount, currencyCode = 'USD') => {
  const currency = getCurrencyByCode(currencyCode);
  return `${currency.symbol}${amount}`;
};

export const isValidCategory = (category) => {
  return templateCategories.includes(category);
};

export const isValidDifficulty = (difficulty) => {
  return difficultyLevels.includes(difficulty);
};

export const isValidType = (type) => {
  return templateTypes.includes(type);
};

export const isValidPlanType = (planType) => {
  return planTypes.includes(planType);
};

export const isValidTechStack = (techStack) => {
  return techStacks.includes(techStack);
};

export const isValidCurrency = (currencyCode) => {
  return currencies.some(c => c.code === currencyCode);
};

